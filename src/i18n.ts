let current = "en-US";

export function currentLocale(): string {
  return current;
}

export function isRTL(code: string = current): boolean {
  return !!LOCALES.find((l) => l.code === code)?.rtl;
}

export function t(key: string): string {
  return dicts[current]?.[key] ?? en[key] ?? key;
}

/** Locale-aware integer formatting (falls back to en-US for unusual codes). */
export function fmtNum(n: number): string {
  try {
    return n.toLocaleString(current);
  } catch {
    return n.toLocaleString("en-US");
  }
}

export function setLocale(code: string): void {
  if (!dicts[code]) code = "en-US";
  current = code;
  localStorage.setItem("locale", code);
  document.documentElement.lang = code;
  document.documentElement.dir = isRTL(code) ? "rtl" : "ltr";
}

function matchNavigator(): string {
  const langs = navigator.languages ?? [navigator.language];
  for (const l of langs) {
    if (dicts[l]) return l;
    const base = l.split("-")[0].toLowerCase();
    const hit = LOCALES.find((x) => x.code.split("-")[0].toLowerCase() === base);
    if (hit) return hit.code;
  }
  return "en-US";
}

export function initLocale(): void {
  const saved = localStorage.getItem("locale");
  setLocale(saved && dicts[saved] ? saved : matchNavigator());
}

/** Apply translations to all tagged elements under `root`. */
export function applyI18n(root: ParentNode = document): void {
  root.querySelectorAll<HTMLElement>("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n!);
  });
  root.querySelectorAll<HTMLInputElement>("[data-i18n-placeholder]").forEach((el) => {
    el.placeholder = t(el.getAttribute("data-i18n-placeholder")!);
  });
  root.querySelectorAll<HTMLElement>("[data-i18n-title]").forEach((el) => {
    el.title = t(el.getAttribute("data-i18n-title")!);
  });
}
